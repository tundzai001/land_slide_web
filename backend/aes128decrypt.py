def xtime(a: int) -> int:
	"""Multiply by x (i.e. 02) in GF(2^8) modulo AES polynomial."""
	if a & 0x80:
		return ((a << 1) ^ 0x1b) & 0xff
	return (a << 1) & 0xff


def gf_mul(a: int, b: int) -> int:
	"""Multiply two bytes in GF(2^8) like the Go implementation."""
	res = 0
	while b != 0:
		if b & 1:
			res ^= a
		a = xtime(a)
		b >>= 1
	return res & 0xff


def expand_key(key: bytes) -> bytearray:
	"""Simplified key expansion matching the Go implementation."""
	if len(key) != 16:
		raise ValueError("key must be 16 bytes")
	ex = bytearray(176)
	ex[0:16] = key
	for i in range(16, 176):
		ex[i] = ex[i - 16] ^ ex[i - 4]
	return ex


def add_round_key(data: bytearray, round_key: bytes) -> bytearray:
	for i in range(len(data)):
		data[i] ^= round_key[i % 16]
	return data


def inv_sub_bytes(data: bytearray) -> bytearray:
	for i in range(len(data)):
		data[i] = InvSBox[data[i]]
	return data


def shift_rows_right(data: bytearray) -> bytearray:
	# Same behavior as the Go version
	for i in range(0, len(data), 4):
		rotations = (i % 16) // 4
		for _ in range(rotations):
			# rotate right the 4-byte chunk
			b0, b1, b2, b3 = data[i], data[i + 1], data[i + 2], data[i + 3]
			data[i], data[i + 1], data[i + 2], data[i + 3] = b3, b0, b1, b2
	return data


def inv_mix_columns(data: bytearray) -> bytearray:
	vector = [0, 0, 0, 0]
	for i in range(0, len(data), 16):
		for j in range(4):
			for l in range(4):
				vector[l] = data[i + j + l * 4]
			for k in range(4):
				v = 0
				for l in range(4):
					v ^= gf_mul(vector[l], invMixtureMatrix[k * 4 + l])
				data[i + j + k * 4] = v
	return data


def remove_pad_from_data(data: bytearray) -> bytearray:
	if not data:
		return data
	padding = data[-1]
	if padding > 16 or padding == 0:
		return data
	# Note: the original Go function checked only first/last pad byte equality
	if data[-1] != data[-padding]:
		return data
	return data[:-padding]


def aes128_decrypt(data: bytearray, initial_key: bytearray = initialKey) -> bytearray:
	expanded_key = expand_key(initial_key)
	# Work on a copy to avoid side effects unless caller expects in-place
	if len(expanded_key) != 176:
		raise ValueError("expanded_key must be 176 bytes")
	# initial round key addition (last round key)
	round_key = bytes(expanded_key[160:176])
	add_round_key(data, round_key)
	for round in range(9, -1, -1):
		shift_rows_right(data)
		inv_sub_bytes(data)
		round_key = bytes(expanded_key[round * 16: round * 16 + 16])
		add_round_key(data, round_key)
		if round != 0:
			inv_mix_columns(data)
	# remove padding as the Go code does
	return remove_pad_from_data(data)

def aes128_decrypt_base64(data: bytearray, initial_key: bytearray = initialKey) -> bytearray:
    data_array = bytearray(base64.b64decode(str(data)))
    return aes128_decrypt(data_array, initial_key)

